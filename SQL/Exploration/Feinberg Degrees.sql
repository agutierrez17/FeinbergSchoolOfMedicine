------ NUMBER OF FEINBERG DEGREES BY YEAR
SELECT
DEGREE_YEAR,
COUNT(*) AS "Number of Degrees"
FROM DEGREES
WHERE
SCHOOL_CODE = 'MED'

GROUP BY
DEGREE_YEAR
ORDER BY
DEGREE_YEAR DESC

